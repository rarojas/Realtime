package com.energyview.realtime.model.dto;

public class SitioInfo {

	private String nombresitio;
	
	private String domicilio;
	
	private String codigopostal;
	
	private String zonaregion;
	
	private String gpslatitud;
	
	private String gpslongitud;

	/**
	 * @return the nombresitio
	 */
	public String getNombresitio() {
		return nombresitio;
	}

	/**
	 * @param nombresitio the nombresitio to set
	 */
	public void setNombresitio(String nombresitio) {
		this.nombresitio = nombresitio;
	}

	/**
	 * @return the domicilio
	 */
	public String getDomicilio() {
		return domicilio;
	}

	/**
	 * @param domicilio the domicilio to set
	 */
	public void setDomicilio(String domicilio) {
		this.domicilio = domicilio;
	}

	/**
	 * @return the codigopostal
	 */
	public String getCodigopostal() {
		return codigopostal;
	}

	/**
	 * @param codigopostal the codigopostal to set
	 */
	public void setCodigopostal(String codigopostal) {
		this.codigopostal = codigopostal;
	}

	/**
	 * @return the zonaregion
	 */
	public String getZonaregion() {
		return zonaregion;
	}

	/**
	 * @param zonaregion the zonaregion to set
	 */
	public void setZonaregion(String zonaregion) {
		this.zonaregion = zonaregion;
	}

	/**
	 * @return the gpslatitud
	 */
	public String getGpslatitud() {
		return gpslatitud;
	}

	/**
	 * @param gpslatitud the gpslatitud to set
	 */
	public void setGpslatitud(String gpslatitud) {
		this.gpslatitud = gpslatitud;
	}

	/**
	 * @return the gpslongitud
	 */
	public String getGpslongitud() {
		return gpslongitud;
	}

	/**
	 * @param gpslongitud the gpslongitud to set
	 */
	public void setGpslongitud(String gpslongitud) {
		this.gpslongitud = gpslongitud;
	}
	
}
